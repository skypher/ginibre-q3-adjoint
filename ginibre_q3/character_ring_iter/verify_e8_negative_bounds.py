def parse_table(path: Path) -> dict[int, tuple[int, Fraction]]:
    lines = path.read_text(errors="strict").splitlines()
    if not lines or lines[0] != "# label|n|expected_cells|negative_bound":
        raise RuntimeError("unexpected E8 negative-bound table header")
    rows: dict[int, tuple[int, Fraction]] = {}
    for line_number, line in enumerate(lines[1:], 2):
        if not line:
            continue
        parts = line.split("|")
        if len(parts) != 4:
            raise RuntimeError(f"malformed table row {line_number}")
        label, n_text, cells_text, bound_text = parts
        n = int(n_text)
        if label != f"n{n}" or n in rows:
            raise RuntimeError(f"bad or duplicate label at row {line_number}")
        bound = Fraction(bound_text)
        if bound <= 0:
            raise RuntimeError(f"nonpositive bound at row {line_number}")
        rows[n] = (int(cells_text), bound)
    if set(rows) != set(EXPECTED_CELLS):
        raise RuntimeError(
            f"E8 negative-bound row set changed: got={sorted(rows)}, "
            f"expected={sorted(EXPECTED_CELLS)}"
        )
    return rows


def worker(n: int) -> int:
    bound, checks = audit_complex_case(n)
    print(json.dumps({"n": n, "bound": str(bound), "checks": checks}, sort_keys=True))
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--table",
        type=Path,
        default=Path(__file__).resolve().parent.parent
        / "certificates/exceptional_rect/e8_rect_negative_bounds.tsv",
    )
    parser.add_argument("--worker", type=int, help=argparse.SUPPRESS)
    args = parser.parse_args()
    if args.worker is not None:
        if args.worker not in COMPLEX_CASES:
            parser.error("worker row is not a complex E8 negative-bound case")
        return worker(args.worker)

    rows = parse_table(args.table)
    for n, expected_cells in EXPECTED_CELLS.items():
        cells, table_bound = rows[n]
        if cells != expected_cells:
            raise RuntimeError(
                f"n={n}: table cell count {cells} != expected {expected_cells}"
            )
        if n in COMPLEX_CASES:
            result = subprocess.run(
                [sys.executable, str(Path(__file__).resolve()), "--worker", str(n)],
                cwd=Path(__file__).resolve().parent,
                check=True,
                text=True,
                capture_output=True,
            )
            payload = json.loads(result.stdout)
            source_bound = Fraction(payload["bound"])
            predicate_count = len(payload["checks"])
        else:
            source_bound = Fraction(2 * (16 * 16 + 4) * 16**n)
            predicate_count = 1
        if source_bound != table_bound:
            raise RuntimeError(
                f"n={n}: table bound does not match exact source calculation"
            )
        print(
            f"n={n} cells={cells} predicates={predicate_count} "
            "bound_match=OK"
        )

    print(f"E8_NEGATIVE_BOUND_TABLE_ROWS={len(rows)}")
    print("E8_NEGATIVE_BOUND_AUDIT: ALL PASS")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
