def require(checks: dict[str, bool]) -> dict[str, bool]:
    if not all(checks.values()):
        failed = [name for name, ok in checks.items() if not ok]
        raise RuntimeError("failed exact predicates: " + ", ".join(failed))
    return checks


def module(name: str):
    return importlib.import_module(name)


